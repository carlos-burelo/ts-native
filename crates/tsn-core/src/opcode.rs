impl OpCode {
    #[inline(always)]
    pub fn from_u16(v: u16) -> Option<Self> {
        if v <= OpCode::OpAssertNotNull as u16 {
            Some(unsafe { std::mem::transmute(v) })
        } else {
            None
        }
    }
}
